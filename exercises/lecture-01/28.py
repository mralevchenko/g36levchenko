import math

# Введення сторін трикутника
a = 5
b = 5
c = 6

# Обчислення периметра
perimeter = a + b + c

# Обчислення напівпериметра
s = (a + b + c) / 2

# Обчислення площі за формулою Герона
area = math.sqrt(s * (s - a) * (s - b) * (s - c))

# Виведення результату
print(f"Периметр трикутника: {perimeter} см")
print(f"Площа трикутника: {area:.2f} см²")

