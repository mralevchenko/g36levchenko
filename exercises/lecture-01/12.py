# Читання двох чисел
a = int(input())
b = int(input())

# Виконання арифметичних операцій
addition = a + b
subtraction = a - b
multiplication = a * b
division = a / b
exponentiation = a ** b

# Виведення результатів через символ "&"
print(f"{addition}&{subtraction}&{multiplication}&{division}&{exponentiation}")

